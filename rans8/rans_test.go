func TestRANS_Encoder_Decoder_Random(t *testing.T) {
	maxLen := 100 * 8

	for i := 1; i < maxLen; i = i + 8 {
		onesCount := i
		expected := make([]int, maxLen)
		values := make(map[int]bool)
		for len(values) < onesCount {
			pos := rand.Intn(maxLen)
			expected[pos] = 1
			values[pos] = true
		}

		freqs := []float64{1 - float64(onesCount)/float64(maxLen), float64(onesCount) / float64(maxLen)}

		ansEncoder := RANSEncoder{
			Freqs: freqs,
		}

		for _, s := range expected {
			ansEncoder.Encode(s)
		}

		actualEncoded := ansEncoder.Bytes()

		ansDecoder := RANSDecoder{
			Freqs:   freqs,
			State:   ansEncoder.State(),
			Encoded: actualEncoded,
		}

		actualDecoded := make([]int, 0)
		s := ansDecoder.Decode()
		for s != -1 {
			actualDecoded = append(actualDecoded, s)
			s = ansDecoder.Decode()
		}

		//reverse the decoded
		for i := 0; i < len(actualDecoded)/2; i++ {
			ii := len(actualDecoded) - 1 - i
			actualDecoded[i], actualDecoded[ii] = actualDecoded[ii], actualDecoded[i]
		}

		if !reflect.DeepEqual(expected, actualDecoded) {
			t.Logf("expected(%v): %v", onesCount, expected)
			t.Fatalf("expected \n%v\n but found \n%v\n", expected, actualDecoded)
		}
	}
}

